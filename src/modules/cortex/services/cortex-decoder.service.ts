  private measureStructureMatch(expectedStructure: any, text: string): number {
    let matchScore = 0.5;

    // Check frame type representation
    const frameTypeWords = expectedStructure.frameType.split(/[^\w]+/);
    const hasFrameTypeWords = frameTypeWords.some((word: string) =>
      text.toLowerCase().includes(word.toLowerCase()),
    );
    if (hasFrameTypeWords) matchScore += 0.2;

    // Check role count appropriateness
    const textLength = text.split(/\s+/).length;
    const expectedLength = expectedStructure.roleCount * 15; // Rough estimate
    const lengthRatio = textLength / expectedLength;

    if (lengthRatio >= 0.5 && lengthRatio <= 2.0) matchScore += 0.2;

    // Check for complex structure indicators
    if (expectedStructure.hasComplexRoles) {
      const complexityIndicators = [
        'including',
        'with',
        'having',
        'containing',
      ];
      const hasComplexityIndicators = complexityIndicators.some((indicator) =>
        text.toLowerCase().includes(indicator),
      );
      if (hasComplexityIndicators) matchScore += 0.1;
    }

    return Math.min(1.0, matchScore);
  }

