  private generateSemanticHash(frame: CortexFrame): string {
    const content = JSON.stringify(frame);
    let hash = 0;
    for (let i = 0; i < content.length; i++) {
      const char = content.charCodeAt(i);
      hash = (hash << 5) - hash + char;
      hash = hash & hash;
    }
    return hash.toString(36);
  }

  private postProcessDecodedText(
    text: string,
    request: CortexDecodingRequest,
  ): string {
    let processed = text;

    // Apply formatting based on request
    if (request.format === 'markdown') {
      processed = this.convertToMarkdown(processed);
    } else if ((request.format as string) === 'html') {
      processed = this.convertToHtml(processed);
    }

    // Apply readability enhancements
    if (request.options?.enhanceReadability) {
      processed = this.enhanceReadability(processed);
    }

    // Apply length constraints
    if (request.options?.maxLength) {
      processed = this.truncateToLength(processed, request.options.maxLength);
    }

    return processed;
  }

  private convertToMarkdown(text: string): string {
    // Convert plain text to markdown format
    let markdown = text;

    // Convert headers (simple heuristic)
    markdown = markdown.replace(/^([A-Z][^.!?]*)$/gm, '# $1');

    // Convert lists
    markdown = markdown.replace(/^(\d+)\.\s+/gm, '$1. ');

    // Convert bullet points
    markdown = markdown.replace(/^[-*]\s+/gm, '- ');

    return markdown;
  }

  private convertToHtml(text: string): string {
    // Convert to basic HTML
    let html = text;

    // Convert line breaks
    html = html.replace(/\n\n/g, '</p><p>');
    html = html.replace(/\n/g, '<br>');

    // Wrap in paragraphs
    html = `<p>${html}</p>`;

    return html;
  }

  private enhanceReadability(text: string): string {
    let enhanced = text;

    // Add paragraph breaks
    enhanced = enhanced.replace(/([.!?])\s+(?=[A-Z])/g, '$1\n\n');

    // Fix spacing issues
    enhanced = enhanced.replace(/\s+/g, ' ');
    enhanced = enhanced.replace(/\s*\n\s*/g, '\n');

    // Capitalize sentence starts
    enhanced = enhanced.replace(
      /(^|[.!?]\s*)([a-z])/g,
      (match, separator, letter) => separator + letter.toUpperCase(),
    );

    return enhanced;
  }

  private truncateToLength(text: string, maxLength: number): string {
    if (text.length <= maxLength) return text;

    // Find a good break point
    let truncated = text.substring(0, maxLength);
    const lastSentenceEnd = Math.max(
      truncated.lastIndexOf('.'),
      truncated.lastIndexOf('!'),
      truncated.lastIndexOf('?'),
    );

    if (lastSentenceEnd > maxLength * 0.8) {
      truncated = truncated.substring(0, lastSentenceEnd + 1);
    }

    return truncated + '...';
  }

  private getPostProcessingSteps(request: CortexDecodingRequest): string[] {
    const steps: string[] = [];

    if (request.format) steps.push(`format_${request.format}`);
    if (request.options?.enhanceReadability)
      steps.push('readability_enhancement');
    if (request.options?.maxLength) steps.push('length_truncation');

    return steps;
  }
