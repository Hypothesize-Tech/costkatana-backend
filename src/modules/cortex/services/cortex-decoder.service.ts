  private countInformationUnits(frame: CortexFrame): number {
    let units = 0;

    for (const [key, value] of Object.entries(frame)) {
      if (key === 'frameType') continue;

      if (typeof value === 'string') {
        units += Math.ceil(value.length / 50); // Roughly 50 chars per info unit
      } else if (Array.isArray(value)) {
        units += value.length;
      } else if (typeof value === 'object') {
        units += Object.keys(value).length;
      }
    }

    return units;
  }

